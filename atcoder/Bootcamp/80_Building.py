"""
2次元累積和
Index考えるのなぜか神経使うので(図も書くから)、時間かかる
しかもTLEする。同じロジックのC++のコードを見つけたので、C++ならACするっぽい。
Pythonでやる場合は、メモ化なり高速化が必要。

H W K V
1 1 200 500
300
--
300 + K*1 = 500 <=500
->1
"""
from itertools import accumulate
def main():
    H,W,K,V=map(int,input().split())
    C = [[0]*(W+1) for _ in range(H+1)]
    for i in range(H):
        C[i+1][1:]=list(map(int,input().split()))
    
    def transpose(X):
        return list(map(list,zip(*X)))
    
    def acc(X,num):
        for i in range(num):
            X[i+1] = list(accumulate(X[i+1]))
        return X
    
    C = acc(C,H)
    C = transpose(C)
    C = acc(C,W)
    C = transpose(C)
    ans = 0
    for i in range(1,H+1+1):
        for j in range(i+1,H+1+1):
            for k in range(1,W+1+1):
                for l in range(k+1,W+1+1):
                    # [i,j] [k,l]
                    a1,a2,b1,b2 = i-1,j-1,k-1,l-1
                    tmp = (j-i)*(l-k)*K + C[a2][b2]-C[a1][b2]-C[a2][b1]+C[a1][b1]
                    if tmp<=V:
                        ans = max(ans,(j-i)*(l-k))
    print(ans)

if __name__=="__main__":
    main()

